# Addition function
def add(a, b):
    return a + b

result = add(5, 5)
print("The sum is:", result)

# Subtraction function
def subtract(a, b):
    return a - b    
result = subtract(10, 5)
print("The difference is:", result)

# Multiplication function
def multiply(a, b):
    return a * b
result = multiply(5, 5)
print("The product is:", result)

# Division function
def divide(a, b):
    return a / b
result = divide(10, 2)
print("The quotient is:", result)

