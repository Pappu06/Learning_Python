
# This is a simple while loop example
for i in range(5):
    print(i)

# This is a simple for loop example
for i in range(0,5):
    print(i)

# This is a simple for loop example with a step value
for i in range(0,5,2):
    print(i)

# This is a simple for loop example with a negative step value
for i in range(5,0,-1):
    print(i)