# lists in pyhton (ordered, mutable, allow duplicates)

fruits = ["apple", "banana", "cherry"]
print(fruits)

# access items
print(fruits[0])  # first item
print(fruits[-1])  # last item

# add items
fruits.append("orange")  # add to end
fruits.append("mango")
print(fruits)

# remove items
fruits.remove("banana")
print(fruits)